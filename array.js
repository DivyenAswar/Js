// Create an array
let colors = ["red", "blue", "green"];

// Access elements
console.log(colors[0]); // red
console.log(colors[1]); // blue

// Add new element
colors.push("yellow");
console.log(colors); // ["red", "blue", "green", "yellow"]

// Remove last element
colors.pop();
console.log(colors); // ["red", "blue", "green"]

// Total number of elements
console.log(colors.length); // 3
